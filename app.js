// Theme management
function initTheme() {
    const savedTheme = localStorage.getItem('sudokuTheme');
    if (savedTheme === 'dark') {
        // Only add dark if explicitly saved
    } else {
        // Default to light mode
        document.documentElement.classList.add('light');
    }
}

function toggleTheme() {
    document.documentElement.classList.toggle('light');
    const isLight = document.documentElement.classList.contains('light');
    localStorage.setItem('sudokuTheme', isLight ? 'light' : 'dark');
}

// Initialize theme immediately to prevent flash
initTheme();

// Initialize the app
async function init() {
    createGrid();
    loadLogFromStorage();
    renderLog();
    
    try {
        updateStatus('Loading C++ solver...', 'loading');
        solverModule = await createSolverModule();
        
        loadTemplates();
        
        updateStatus('Ready to solve!', 'ready');
        document.getElementById('solve-btn').disabled = false;
        document.getElementById('bench-start-btn').disabled = false;
    } catch (error) {
        updateStatus('Error loading solver: ' + error.message, 'error');
        console.error(error);
    }
    
    setupEventListeners();
}

function updateStatus(message, state) {
    const statusBar = document.getElementById('status-bar');
    const statusText = document.getElementById('status-text');
    const loader = document.getElementById('loader');
    
    statusText.textContent = message;
    statusBar.className = 'status-bar ' + state;
    
    if (state === 'loading') {
        loader.classList.remove('hidden');
    } else {
        loader.classList.add('hidden');
    }
}

function createGrid() {
    const grid = document.getElementById('sudoku-grid');
    grid.innerHTML = '';
    
    for (let i = 0; i < 81; i++) {
        const input = document.createElement('input');
        input.type = 'text';
        input.maxLength = 1;
        input.dataset.index = i;
        input.addEventListener('input', handleCellInput);
        input.addEventListener('keydown', handleKeyNavigation);
        grid.appendChild(input);
    }
}

function handleCellInput(e) {
    const value = e.target.value;
    if (value && !/^[1-9]$/.test(value)) {
        e.target.value = '';
    }
    e.target.classList.remove('solved', 'invalid');
}

function handleKeyNavigation(e) {
    const index = parseInt(e.target.dataset.index);
    const inputs = document.querySelectorAll('.sudoku-grid input');
    let newIndex = index;
    
    switch(e.key) {
        case 'ArrowUp': newIndex = index - 9; break;
        case 'ArrowDown': newIndex = index + 9; break;
        case 'ArrowLeft': newIndex = index - 1; break;
        case 'ArrowRight': newIndex = index + 1; break;
        default: return;
    }
    
    if (newIndex >= 0 && newIndex < 81) {
        inputs[newIndex].focus();
        e.preventDefault();
    }
}


